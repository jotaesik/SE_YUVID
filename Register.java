public class Register {
   

   private String register_id;
   private String register_pw;
   private String register_check_pw;
   private String register_name;
   
   private String register_phone_number;

   public String getRegister_id() {
      return register_id;
   }

   public void setRegister_id(String register_id) {
      this.register_id = register_id;
   }

   public String getRegister_pw() {
      return register_pw;
   }

   public void setRegister_pw(String register_pw) {
      this.register_pw = register_pw;
   }

   public String getRegister_check_pw() {
      return register_check_pw;
   }

   public void setRegister_check_pw(String register_check_pw) {
      this.register_check_pw = register_check_pw;
   }

   public String getRegister_name() {
      return register_name;
   }

   public void setRegister_name(String register_name) {
      this.register_name = register_name;
   }

   


   public String getRegister_phone_number() {
      return register_phone_number;
   }

   public void setRegister_phone_number(String register_phone_number) {
      this.register_phone_number = register_phone_number;
   }
}